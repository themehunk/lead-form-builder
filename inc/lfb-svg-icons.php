/**
 * Return an SVG icon string.
 *
 * @param string $icon  Icon key (e.g. 'trash', 'edit', 'close').
 * @param int    $size  Optional. Override both width and height (px).
 * @return string       SVG markup or empty string if icon not found.
 */
function lfb_svg( string $icon, int $size = 0 ): string {
    static $icons = null;
    if ( $icons === null ) {
        $icons = lfb_get_svg_icons();
    }
    if ( ! array_key_exists( $icon, $icons ) ) {
        return '';
    }
    $svg = $icons[ $icon ];
    if ( $size > 0 ) {
        $svg = preg_replace( '/\bwidth="[^"]*"/',  'width="'  . $size . '"', $svg, 1 );
        $svg = preg_replace( '/\bheight="[^"]*"/', 'height="' . $size . '"', $svg, 1 );
    }
    return $svg;
}
